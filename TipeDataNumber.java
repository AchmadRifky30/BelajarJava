public class TipeDataNumber {

    public static void main(String[] args) {
        // Tipe Data Integer

        byte varByte = 122;
        short varShort = 10000;
        int varInt = 10000000;
        long varLong = 100000000;
        long varLong2 = 100000000000L;

        // Floating point number

        float varFloat = 10.10F; // 4 digit
        double varDouble = 12.242; // 15 digit

        // kode literals

        int decimalInt = 34;
        int hexaDecimal = 0xFFFF; // A = 10, B = 11, C = 12, D = 13, E = 14, F = 15
        int bynariDecimal = 0b010101;

        // menambah underscore

        int amount = 60_000_000;
        long amount2 = 1_000_000_000_000L;

    }
}

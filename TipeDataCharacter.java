public class TipeDataCharacter {

    public static void main(String[] args) {

        // digunakan untuk data tunggal

        char s = 'S';
        char h = 'H';
        char a = 'A';
        char n = 'N';
        char d = 'D';
        char y = 'Y';

        char o = 'O';
        char z = 'Z';

        // kalau pakai (LN) huruf nya turun kebawah

        System.out.println(s);
        System.out.println(h);
        System.out.println(a);
        System.out.println(n);
        System.out.println(d);
        System.out.println(y);

        // kalau tidak pakai (LN) huruf nya tidak ber spasi ke bawah  - itu perbedaannya

        System.out.print(o);
        System.out.print(z);


    }
}
